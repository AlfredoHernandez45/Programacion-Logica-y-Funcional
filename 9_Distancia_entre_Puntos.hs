-- Define la funcion "distancia (x1,y1) (x2,y2)", la cual recibe 2 cordenadas 
-- X y Y, estas son aplicadas a la ecuacion para calcular la distancia.
--
-- Por ejemplo:
-- distancia (2,3) (9,10) = 9.899494936611665

distancia (x1,y1) (x2,y2) = sqrt((x1-x2)^2+(y1-y2)^2)